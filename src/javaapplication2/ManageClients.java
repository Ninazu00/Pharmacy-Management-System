    private void DeleteClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteClientActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a client to delete.");
            return;
        }

        String clientId = jTable1.getValueAt(selectedRow, 0).toString();

        Client toDelete = null;
        for (Client client : Client.getClients()) 
        {
            if (client.getId().equals(clientId)) 
            {
                toDelete = client;
                break;
            }
        }

        if (toDelete != null) 
        {
            int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this client?", "Confirm Deletion", javax.swing.JOptionPane.YES_NO_OPTION);
            if (confirm == javax.swing.JOptionPane.YES_OPTION) 
            {
                Client.getClients().remove(toDelete);
                refreshTable();
                javax.swing.JOptionPane.showMessageDialog(this, "Client deleted successfully!");
            }
        } 
        else 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Selected client not found.");
        }
    }//GEN-LAST:event_DeleteClientActionPerformed

    private void AddClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddClientActionPerformed
        String id = ClientID.getText().trim();
        String name = ClientName.getText().trim();
        String username = ClientUsername.getText().trim();
        String email = ClientEmail.getText().trim();
        String phone = ClientPhone.getText().trim();
        String address = ClientAddress.getText().trim();
        String password = ClientPassword.getText().trim();

        // Validation
        if (id.isEmpty() || name.isEmpty() || username.isEmpty() || email.isEmpty() || phone.isEmpty() || address.isEmpty() || password.isEmpty()) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        if (!email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid email format.");
            return;
        }

        if (!phone.matches("\\d+")) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Phone number must contain digits only.");
            return;
        }

        // Check if username already exists
        for (Client client : Client.getClients()) 
        {
            if (client.getUsername().equals(username)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Username already exists. Please choose another.");
                return;
            }
        }

        // Create a new client and add it to the list
        Client newClient = new Client(id, name, username, email, phone, password, address);
        Client.addClient(newClient);

        refreshTable();
        clearFields();

        javax.swing.JOptionPane.showMessageDialog(this, "Client added successfully!");
    }//GEN-LAST:event_AddClientActionPerformed

