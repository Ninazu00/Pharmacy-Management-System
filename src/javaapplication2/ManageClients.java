    private void refreshTable() 
    {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Client client : Client.getClients()) 
        {
            model.addRow(new Object[]{client.getId(), client.getName(), client.getUsername(), client.getEmail(), client.getPhone(), client.getAddress(), client.getPassword()});
        }
    }
    
    private void setupPlaceholder(JTextField textField, String placeholder) {
        Font fontUsed = new Font("Gadugi", Font.BOLD, 14);

        textField.setForeground(Color.GRAY);
        textField.setFont(fontUsed);
        textField.setText(placeholder);

        textField.addFocusListener(new java.awt.event.FocusAdapter() 
        {
            public void focusGained(java.awt.event.FocusEvent evt) 
            {
                if (textField.getText().equals(placeholder)) 
                {
                    textField.setText("");
                    textField.setForeground(Color.BLACK);
                    textField.setFont(fontUsed);
                }
            }

            public void focusLost(java.awt.event.FocusEvent evt) 
            {
                if (textField.getText().isEmpty()) 
                {
                    textField.setForeground(Color.GRAY);
                    textField.setFont(fontUsed);
                    textField.setText(placeholder);
                }
            }
        });
    }
    
    private void setupPasswordPlaceholder(JPasswordField passwordField, String placeholder) {
        Font fontUsed = new Font("Gadugi", Font.BOLD, 14);

        passwordField.setForeground(Color.GRAY);
        passwordField.setFont(fontUsed);
        passwordField.setEchoChar((char)0); // Show actual text instead of '*'
        passwordField.setText(placeholder);

        passwordField.addFocusListener(new java.awt.event.FocusAdapter() 
        {
            public void focusGained(java.awt.event.FocusEvent evt) 
            {
                if (String.valueOf(passwordField.getPassword()).equals(placeholder)) 
                {
                    passwordField.setText("");
                    passwordField.setForeground(Color.BLACK);
                    passwordField.setFont(fontUsed);
                    passwordField.setEchoChar('*'); // Switch back to '*' when typing
                }
            }

            public void focusLost(java.awt.event.FocusEvent evt) 
            {
                if (passwordField.getPassword().length == 0) 
                {
                    passwordField.setForeground(Color.GRAY);
                    passwordField.setFont(fontUsed);
                    passwordField.setEchoChar((char)0); // Show actual text again
                    passwordField.setText(placeholder);
                }
            }
        }
        );
    }
    
