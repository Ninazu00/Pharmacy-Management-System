    private void refreshTable() 
    {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Medicine medicine : Medicine.getAllMedicines()) 
        {
            model.addRow(new Object[]{medicine.getId(), medicine.getName(), medicine.getBatchNo(), medicine.getPrice(), medicine.getQuantity(), medicine.getExpiryDate()});
        }
    }
    
