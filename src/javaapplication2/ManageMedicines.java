    private void AddMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddMedicineActionPerformed
        String id = MedicineID.getText().trim();
        String name = MedicineName.getText().trim();
        String batchNo = MedicineBatchNumber.getText().trim();
        String priceStr = MedicinePrice.getText().trim();
        String quantityStr = MedicineQuantity.getText().trim();
        String expiryDate = MedicineExpiryDate.getText().trim();

        // Validate input
        if (id.isEmpty() || name.isEmpty() || batchNo.isEmpty() || priceStr.isEmpty() || quantityStr.isEmpty() || expiryDate.isEmpty()) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        // Validatation
        try 
        {
            int medicineId = Integer.parseInt(id);
            double price = Double.parseDouble(priceStr);
            int quantity = Integer.parseInt(quantityStr);

            if (price <= 0 || quantity <= 0) 
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Price and Quantity must be positive numbers.");
                return;
            }

            // Validate date format (simple validation)
            if (!expiryDate.matches("\\d{4}-\\d{2}-\\d{2}")) 
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Invalid date format. Please use YYYY-MM-DD.");
                return;
            }

            // Check if medicine with same ID already exists
            if (Medicine.findMedicineById(medicineId) != null) 
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Medicine with this ID already exists.");
                return;
            }

            // Create and add new medicine
            Date expiry = java.sql.Date.valueOf(expiryDate);
            Medicine newMedicine = new Medicine(medicineId, name, batchNo, price, quantity, expiry);
            Medicine.addMedicine(newMedicine);

            refreshTable();
            clearFields();
            javax.swing.JOptionPane.showMessageDialog(this, "Medicine added successfully!");

        } 
        catch (NumberFormatException e) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "ID, Price and Quantity must be valid numbers.");
        } 
        catch (IllegalArgumentException e) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid date format. Please use YYYY-MM-DD.");
        }
    }//GEN-LAST:event_AddMedicineActionPerformed

    private void UpdateMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateMedicineActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a medicine to update.");
            return;
        }

        String id = MedicineID.getText().trim();
        String name = MedicineName.getText().trim();
        String batchNo = MedicineBatchNumber.getText().trim();
        String priceStr = MedicinePrice.getText().trim();
        String quantityStr = MedicineQuantity.getText().trim();
        String expiryDate = MedicineExpiryDate.getText().trim();

        // Validate input
        if (id.isEmpty() || name.isEmpty() || batchNo.isEmpty() || priceStr.isEmpty() || quantityStr.isEmpty() || expiryDate.isEmpty()) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        try 
        {
            int medicineId = Integer.parseInt(id);
            double price = Double.parseDouble(priceStr);
            int quantity = Integer.parseInt(quantityStr);

            if (price <= 0 || quantity <= 0) 
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Price and Quantity must be positive numbers.");
                return;
            }

            // Validate date format
            if (!expiryDate.matches("\\d{4}-\\d{2}-\\d{2}")) 
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Invalid date format. Please use YYYY-MM-DD.");
                return;
            }

            // Create updated medicine
            Date expiry = java.sql.Date.valueOf(expiryDate);
            Medicine updatedMedicine = new Medicine(medicineId, name, batchNo, price, quantity, expiry);

            // Update the medicine
            boolean success = Medicine.updateMedicine(medicineId, updatedMedicine);

            if (success) 
            {
                refreshTable();
                clearFields();
                javax.swing.JOptionPane.showMessageDialog(this, "Medicine updated successfully!");
            }
            else 
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Failed to update medicine.");
            }

        }
        catch (NumberFormatException e) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "ID, Price and Quantity must be valid numbers.");
        }
        catch (IllegalArgumentException e) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid date format. Please use YYYY-MM-DD.");
        }
    }//GEN-LAST:event_UpdateMedicineActionPerformed

    private void DeleteMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteMedicineActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a medicine to delete.");
            return;
        }

        int medicineId = (int) jTable1.getValueAt(selectedRow, 0);

        Medicine medicineToRemove = Medicine.findMedicineById(medicineId);

        if (medicineToRemove != null) 
        {
            int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this medicine?", "Confirm Deletion", javax.swing.JOptionPane.YES_NO_OPTION);

            if (confirm == javax.swing.JOptionPane.YES_OPTION) 
            {
                Medicine.removeMedicine(medicineId);
                refreshTable();
                javax.swing.JOptionPane.showMessageDialog(this, "Medicine deleted successfully!");
            }
        } 
        else 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Selected medicine not found.");
        }
    }//GEN-LAST:event_DeleteMedicineActionPerformed

